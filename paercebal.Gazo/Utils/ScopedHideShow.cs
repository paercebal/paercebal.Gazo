namespace paercebal.Gazo.Utils
{
    public class ScopedHideShow : IDisposable
    {
        private Window window;

        public ScopedHideShow(Window window_)
        {
            this.window = window_;
            this.window.Hide();
        }

        #region IDisposable Support
        private bool disposedValue = false;

        protected virtual void Dispose(bool disposing)
        {
            if (!disposedValue)
            {
                if (disposing)
                {
                    this.window.Show();
                }

                disposedValue = true;
            }
        }

        public void Dispose()
        {
            Dispose(true);
        }
        #endregion
    }
}
